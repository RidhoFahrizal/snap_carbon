import 'package:flutter/material.dart';
class AppSpacing {
  // Base spacing unit (4px)
  static const double unit = 4.0;

  // Spacing values
  static const double xs = unit; // 4px
  static const double sm = unit * 2; // 8px
  static const double md = unit * 4; // 16px
  static const double lg = unit * 6; // 24px
  static const double xl = unit * 8; // 32px
  static const double xl2 = unit * 12; // 48px
  static const double xl3 = unit * 16; // 64px
  static const double xl4 = unit * 24; // 96px
  static const double xl5 = unit * 32; // 128px

  // Insets
  static const double insetXs = xs;
  static const double insetSm = sm;
  static const double insetMd = md;
  static const double insetLg = lg;
  static const double insetXl = xl;
}